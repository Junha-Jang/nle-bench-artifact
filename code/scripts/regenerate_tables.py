"""Regenerate reviewer-facing result tables.

This script is intentionally dependency-free. By default it reads the
reviewer-facing recomputable aggregate CSV and prints a compact ranked table
for the clean canonical paper row set: reconciled full-800 tool-use rows,
excluding attempted-only context-window-overflow rows. Use
``--row-set recomputable`` to include every reconciled aggregate row, including
attempted-only rows and direct-generation rows.

For paper-relevant checks, ``--table row-filter`` lists each aggregate row's
status, and ``--table per-class --row-set paper-main`` recomputes a
Table-2-style per-class summary from redacted per-scenario validation fields.
The redacted artifact does not include response-level refusal-audit judge
labels, so SR-infeasible values emitted here are detector provenance rather
than recovered audit labels.

``--table error-types`` summarizes the redacted ``error_message_type`` field
into coarse provider/runtime categories. It is not a validated failure-mode
taxonomy: rows without an exception can still be constraint failures,
calibration-behavior failures, malformed-but-parseable tool plans, or task
misunderstandings that require raw traces to separate.

The auditable ``supplementary/results/run_manifest.csv`` is generated by
``generate_review_artifacts.py`` from matched raw run summaries. For debugging
legacy aggregate rows, pass ``--write-preview-manifest <path>`` to write an
expected-path preview manifest without replacing the reviewed manifest.
"""

